static int panel_enable(struct aic_panel *panel)
{
	struct xm91080 *xm91080 = panel_to_xm91080(panel);

	panel_di_enable(panel, 0);

	aic_delay_ms(20);
	gpiod_set_value(xm91080->reset, 1);
	aic_delay_ms(1);
	gpiod_set_value(xm91080->reset, 0);
	aic_delay_ms(10);
	gpiod_set_value(xm91080->reset, 1);
	aic_delay_ms(120);

	panel_dsi_send_perpare(panel);
	panel_send_command(init_sequence, ARRAY_SIZE(init_sequence), panel);
	panel_dsi_setup_realmode(panel);

	panel_de_timing_enable(panel, 0);
	panel_backlight_enable(panel, 0);
	return 0;
}

static int panel_disable(struct aic_panel *panel)
{
	struct xm91080 *xm91080 = panel_to_xm91080(panel);

	panel_default_disable(panel);

	gpiod_set_value(xm91080->reset, 0);
	aic_delay_ms(10);

	return 0;
}

static struct aic_panel_funcs panel_funcs = {
	.disable = panel_disable,
	.unprepare = panel_default_unprepare,
	.prepare = panel_default_prepare,
	.enable = panel_enable,
	.get_video_mode = panel_default_get_video_mode,
	.register_callback = panel_register_callback,
};

/* Init the videomode parameter, dts will override the initial value. */
static struct videomode panel_vm = {
	.pixelclock = 130000000,
	.hactive = 1080,
	.hfront_porch = 160,
	.hback_porch = 160,
	.hsync_len = 40,
	.vactive = 1920,
	.vfront_porch = 10,
	.vback_porch = 20,
	.vsync_len = 8,
	.flags = DISPLAY_FLAGS_HSYNC_LOW | DISPLAY_FLAGS_VSYNC_LOW |
		DISPLAY_FLAGS_DE_HIGH | DISPLAY_FLAGS_PIXDATA_POSEDGE
};

static struct panel_dsi dsi = {
	.format = DSI_FMT_RGB888,
	.mode = DSI_MOD_VID_PULSE,
	.lane_num = 4,
};

static int panel_bind(struct device *dev, struct device *master, void *data)
{
	struct panel_comp *p;
	struct xm91080 *xm91080;

	p = devm_kzalloc(dev, sizeof(*p), GFP_KERNEL);
	if (!p)
		return -ENOMEM;

	xm91080 = devm_kzalloc(dev, sizeof(*xm91080), GFP_KERNEL);
	if (!xm91080)
		return -ENOMEM;

	xm91080->reset = devm_gpiod_get(dev, "reset", GPIOD_ASIS);
	if (IS_ERR(xm91080->reset)) {
		dev_err(dev, "failed to get reset gpio\n");
		return PTR_ERR(xm91080->reset);
	}

	if (panel_parse_dts(p, dev) < 0)
		return -1;

	p->panel.dsi = &dsi;
	panel_init(p, dev, &panel_vm, &panel_funcs, xm91080);

	dev_set_drvdata(dev, p);
	return 0;
}

static const struct component_ops panel_com_ops = {
	.bind	= panel_bind,
	.unbind	= panel_default_unbind,
};

static int panel_probe(struct platform_device *pdev)
{
	dev_info(&pdev->dev, "%s()\n", __func__);
	return component_add(&pdev->dev, &panel_com_ops);
}

static int panel_remove(struct platform_device *pdev)
{
	component_del(&pdev->dev, &panel_com_ops);
	return 0;
}

